    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        processRequest(request, response);
    }

    
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
         BlogDao dao = new BlogDao();

        String author = request.getParameter("author");
        String image = request.getParameter("blog_img");

        long millis = System.currentTimeMillis();
        java.sql.Date date = new java.sql.Date(millis);

        //Date date = new Date();
        String title = request.getParameter("title");
        String description = request.getParameter("description");
        int creator_id = Integer.parseInt(request.getParameter("creator_id"));
        String content = request.getParameter("blog_content");
        Blog blogNew = new Blog(title, author, description, date, image, creator_id, content);

        dao.createBlog(blogNew);

        response.sendRedirect("blog");
    }

